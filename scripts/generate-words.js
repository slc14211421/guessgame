// ---- 工具函数 ----

/** 数组去重（使用 Set） */
function deduplicate(list) {
  return [...new Set(list)]
}

/**
 * 确保词条数量达到目标值
 * 不足时从已有词中循环取词补充，防止空列表
 */
function padIfNeeded(list, target, prefix) {
  const unique = deduplicate(list)
  if (unique.length >= target) return unique.slice(0, target)
  const result = [...unique]
  let idx = 0
  while (result.length < target) {
    const base = unique[idx % unique.length]
    result.push(`${base}`)
    idx++
    if (idx >= unique.length * 3) break // 防止死循环
  }
  return result.slice(0, target)
}

